
# operadores aritiméticos
# + = adição
# - = subitração
# * = multiplicação
# / = divisão
# ** = potenciação
# // = divisão inteira
# % = resto da divisão

# ordem de precedência
# 1º = ()
# 2º = **
# 3º = *, /, //, %
# 4º = +, -

# potência de um número
# 9²
# pow(9, 2)
print(f'9 ao quadrado é - {pow(9, 2)}')
# ou
# 9**2
print(f'9 ao quadrado é - {9**2}')

# raiz quadrada
# raiz de 81
# 81**(1/2)
print(f'Raiz quadrada de 81 é - {81**(1/2)}')

# raiz cúbica
# raiz cúbica de 125
# 125**(1/3)
print(f'Raiz cúbica de 125 é - {125**(1/3)}')
