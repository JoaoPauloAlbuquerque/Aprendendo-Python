# Convertendo para string.
s = str(input('Digite um texto: '))
print(f'Você digitou - {s}')

# Convertendo para int.
n = int(input('Digite um número inteiro: '))
print(f'Você digitou - {n}')

# Convertendo para float.
n1 = float(input("Digite um número float: "))
print(f'Você digitou - {n1}')

# Convertendo para bool.
# Neste caso, se for digitado algo,
# o valor da variável será True,
# caso contrário, será False.
v = bool(input('Digite um algo: '))
print(f'Você digitou? {v}')
